// Records Search
function recordsSearch() {
	const searchInput = document.querySelector('.home-search-field');

	if (tableBody) {
		function findRecordItem(term, records) {
			return records.filter((item) => {
				const regex = new RegExp(term, 'gi');

				return (
					item.lastName.match(regex) ||
					item.givenNames.match(regex) ||
					item.yearOfBirth.toString().match(regex) ||
					item.yearOfDeath.toString().match(regex) ||
					item.intermentType.match(regex) ||
					item.plotNumber.match(regex) ||
					item.leaseExpiration.toString().match(regex)
				);
			});
		}

		function displayMatches() {
			const matchArray = findRecordItem(this.value, recordList);
			const tableBodyChild = tableBody.querySelector('tr');

			let html = matchArray
				.map((item) => {
					const regex = new RegExp(this.value, 'gi');
					const lastName = item.lastName.replace(
						regex,
						`<span class="hl">${this.value}</span>`
					);
					const givenNames = item.givenNames.replace(
						regex,
						`<span class="hl">${this.value}</span>`
					);
					const yearOfBirth = item.yearOfBirth
						.toString()
						.replace(regex, `<span class="hl">${this.value}</span>`);
					const yearOfDeath = item.yearOfDeath
						.toString()
						.replace(regex, `<span class="hl">${this.value}</span>`);
					const intermentType = item.intermentType.replace(
						regex,
						`<span class="hl">${this.value}</span>`
					);
					const plotNumber = item.plotNumber.replace(
						regex,
						`<span class="hl">${this.value}</span>`
					);
					const leaseExpiration = item.leaseExpiration
						.toString()
						.replace(regex, `<span class="hl">${this.value}</span>`);

					return `
					<tr>
						<td>${lastName}</td>
						<td>${givenNames}</td>
						<td>${yearOfBirth}</td>
						<td>${yearOfDeath}</td>
						<td>${intermentType}</td>
						<td>${plotNumber}</td>
						<td>${leaseExpiration}</td>
					</tr>
		`;
				})
				.join('');
			tableBody.innerHTML = html;
		}

		searchInput.addEventListener('change', displayMatches);
		searchInput.addEventListener('keyup', displayMatches);
	}
}

