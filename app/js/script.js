function selectOption() {
	const btnBurial = document.querySelector('#btn-burial');
	const btnAshes = document.querySelector('#btn-ashes');
	const relinquishForm = document.querySelector('.relinquish-form');

	if (relinquishForm) {
		function selectBurial() {
			relinquishForm.classList.add('burial-active');
			relinquishForm.classList.remove('ashes-active');
			btnBurial.classList.add('active');
			btnAshes.classList.remove('active');
		}
		function selectAshes() {
			relinquishForm.classList.add('ashes-active');
			relinquishForm.classList.remove('burial-active');
			btnAshes.classList.add('active');
			btnBurial.classList.remove('active');
		}

		selectBurial();
		btnBurial.addEventListener('click', selectBurial);
		btnAshes.addEventListener('click', selectAshes);
	}
}

function signature() {
	const canvas = document.querySelector('.form-control-canvas');
	const clearBtn = document.querySelector('.form-group-canvas--btn-clear');

	if (canvas) {
		var signaturePad = new SignaturePad(canvas);

		function resizeCanvas() {
			var ratio = Math.max(window.devicePixelRatio || 1, 1);
			canvas.width = canvas.offsetWidth * ratio;
			canvas.height = canvas.offsetHeight * ratio;
			canvas.getContext('2d').scale(ratio, ratio);
			signaturePad.clear(); // otherwise isEmpty() might return incorrect value
		}

		clearBtn.addEventListener('click', (e) => {
			e.preventDefault();
			signaturePad.clear();
		});

		window.addEventListener('resize', resizeCanvas);
		resizeCanvas();
	}
}

